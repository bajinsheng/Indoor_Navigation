	    public void SetSP(int sp)//设置起点
	    {
		    this.startpoint = sp;
		    for (v = 0; v < 299; v++)
	         {
	              Final[v] = 0;
	              D[v] = arcs[sp,v];
	              if(D[v] == Double.MaxValue)
	                t[v] = -1;
	              else
	                t[v] = sp;
	         }
	         D[sp] = 0;
	         Final[sp] = 1;
	    }
        public void Dijkstra()//迪杰斯特拉算法路径规划
	    {
	         for (int i = 1; i < 299; i++)
	         {
	              double min = Double.MaxValue;
	              for (w = 0; w < 299; w++)
	              {
	                   if (Final[w] == 0) 
	                   {
	                        if (D[w] < min) {v = w; min = D[w];}
	                   }
	              }
	              Final[v] = 1;
	              for (w = 0; w < 299; w++)
	              {
	                   if ((Final[w] == 0) && (min+arcs[v,w] < D[w]))
	                   {
	                        D[w] = min + arcs[v,w];
	                        t[w] = v;
	                   }
	              }
	         }
	    }
        public List<int> OutPoint(int end)//输出结果
	    {
            List<int> r = new List<int>();
		    while(end != startpoint)
		    {
			    r.Add(end);
			    end = t[end];
		    }
		    r.Add(end);
            r.Reverse();
		    return r;
	    }
